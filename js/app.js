let lastScroll = 0;
window.addEventListener('scroll', pc_scroll);
function pc_scroll() {
	let st = pageYOffset;
	let mobLink = document.querySelector('.pc-links');
	if (st > lastScroll) {
		mobLink.style.setProperty('top', `calc(50% - ${(st-lastScroll)*7}px)`);
	} else {
		mobLink.style.setProperty('top', `calc(50% + ${-((st-lastScroll)*7)}px)`);
	}
	lastScroll = st;
	if (st == lastScroll) {
		setTimeout(function () {
			mobLink.style.setProperty('top', '50%');
		}, 100);
	}
}

